package java_LPU;

public class literals {

    public static void main(String[] args){

        int decVar = 101; // decimal base 10
        int binVar = 0b1111;//binary base 2
        int octVar = 0146;// octal base 8
        int hexVar = 0x123Face; // hexadecimal base 16

        float floatVar = 5.005f;
        double doubleVar = 5.8;

        char charVar = 'A';
        char uniChar = '\u0061';

        String strVar = "hello Java";



        System.out.println(decVar);
        System.out.println(binVar);
        System.out.println(octVar);
        System.out.println(hexVar);
        System.out.println(floatVar);
        System.out.println(doubleVar);
        System.out.println(charVar);
        System.out.println(uniChar);
        System.out.println(strVar);

    }

}
